// QBIT: quadruple bit

#define MAKE_BIT(_a)                                (!!(_a))
#define MAKE_QBIT(_a, _b, _c, _d)                   ((MAKE_BIT(_a) << 3) | (MAKE_BIT(_b) << 2) | (MAKE_BIT(_c) << 1) | MAKE_BIT(_d))

#define GET_BIT(_val, _i)                           (((_val) >> _i) & 1)
#define PUT_BIT(_var, _i)                           ((_var) |= (1 << (_i)))
#define CLR_BIT(_var, _i)                           ((_var) &= ~(1 << (_i)))
#define SET_BIT(_var, _i, _val)                     (MAKE_BIT(_val) ? PUT_BIT(_var, _i) : CLR_BIT(_var, _i))

#define LOQBIT(_val)                                ((_val) & 0x0f)
#define HIQBIT(_val)                                (((_val) >> 4) & 0x0f)

// prevent duplication with definitions in windows.h
#ifndef LOBYTE
#define LOBYTE(_val)                                ((_val) & 0xff)
#endif //LOBYTE
#ifndef HIBYTE
#define HIBYTE(_val)                                (((_val) >> 8) & 0xff)
#endif //HIBYTE
#ifndef LOWORD
#define LOWORD(_val)                                ((_val) & 0xffff)
#endif //LOWORD
#ifndef HIWORD
#define HIWORD(_val)                                (((_val) >> 16) & 0xffff)
#endif //HIWORD

#define MAKE_BYTE(_a, _b, _c, _d, _e, _f, _g, _h)   ((MAKE_QBIT(_a, _b, _c, _d) << 4) | MAKE_QBIT(_e, _f, _g, _h))
#define MAKE_WORD(_h, _l)                           ((LOBYTE(_h) << 8) | LOBYTE(_l))
#define MAKE_DWORD(_h, _l)                          ((LOWORD(_h) << 16) | LOWORD(_l))

#define FILL(_var)                                  ((_var) = ~((_var) ^ (_var)))
#define CLEAR(_var)                                 ((_var) ^= (_var))



